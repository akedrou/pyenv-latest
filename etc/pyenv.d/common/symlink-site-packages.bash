symlink_site_packages() {
    local minor_version=""
    local site_packages=""
    PYTHON_LATEST_ROOT=${PYTHON_LATEST_ROOT:-"$PYENV_ROOT/plugins/pyenv-latest"}
    mkdir -p "${PYTHON_LATEST_ROOT}/site-packages"

    case $VERSION_NAME in
    [23]"."* )
    shopt -s nullglob
    for f in "${PYENV_ROOT}/versions/${1}"/lib/python*; do
      minor_version=${f#"${PYENV_ROOT}/versions/${1}/lib/python"}
      site_packages="$f/site-packages"
      break
    done
    shopt -u nullglob
    ;;
    *) ;;
    esac
    if [ ! -z "$minor_version" ]; then
      if [ ! -d "${PYTHON_LATEST_ROOT}/site-packages/${minor_version}" ]; then
        mv $site_packages "${PYTHON_LATEST_ROOT}/site-packages/${minor_version}"
        ln -sf "${PYTHON_LATEST_ROOT}/site-packages/${minor_version}" $site_packages
      else
        rm -rf $site_packages
        ln -sf "${PYTHON_LATEST_ROOT}/site-packages/${minor_version}" $site_packages
      fi
    fi
}
